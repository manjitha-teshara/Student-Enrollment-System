    private void submitbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitbtnActionPerformed
        
    String resitNo=resitNotxt.getText();
    String nameWithIntials=nameWithIntialstxt.getText();
    String regNo=studentRegNotxt.getText();
    String indexNo=studentIndexNotxt.getText();
    Double value=Double.parseDouble(valuetxt.getText());
    String semesterId=semesterIdtxt.getText();
    PaymentModel paymentModel=new PaymentModel(resitNo, nameWithIntials, regNo, indexNo, value, semesterId);
     try{
             int res = PaymentController.addPayemnt(paymentModel);
            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Added Success");
            }        }
        catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "No Driver found");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());

        }
        catch (Exception e){}
     
    }//GEN-LAST:event_submitbtnActionPerformed

    private void SearchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchbtnActionPerformed
         String resitId=resitNotxt.getText();
        
        try{
             PaymentModel paymentModel = PaymentController.searchPayment(resitId);
            if (paymentModel != null) {
                nameWithIntialstxt.setText(paymentModel.getNameWithIntials());
                studentRegNotxt.setText(paymentModel.getRegNo());
                studentIndexNotxt.setText(paymentModel.getIndexNo());
                valuetxt.setText(Double.toString(paymentModel.getValue()));
                semesterIdtxt.setText(paymentModel.getSemesterId());
                
                
               
            } else {
                JOptionPane.showMessageDialog(this, "No Such payment ");
            }        }
        catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "No Driver found");
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(this, ex.getMessage());
           System.out.println(ex);
        }
                catch (Exception e){}

    }//GEN-LAST:event_SearchbtnActionPerformed

